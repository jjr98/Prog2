package de.fhws.roemischj.Prog2.lektion18.uebung1;

public class MatrikelNummerException extends Exception {

	public MatrikelNummerException() {
		super();
	}

	public MatrikelNummerException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public MatrikelNummerException(String message, Throwable cause) {
		super(message, cause);
	}

	public MatrikelNummerException(Throwable cause) {
		super(cause);
	}

	public MatrikelNummerException(String message) {
		super(message);
	}

	/**
	 * 
	 */
	private static final long serialVersionUID = 352757366328356890L;

}
